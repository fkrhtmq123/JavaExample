package example;

public class Code14 {
	
	public static void main(String[] args) {
		short s = 32767;
		System.out.println(s);
		s++;
		System.out.println(s);
		s++;
		System.out.println(s);
		
		short S = -32767;
		System.out.println(S);
		S--;
		System.out.println(S);
		S--;
		System.out.println(S);
	}

}
